def _normalize_pose_text(pose_text: str) -> str:
    for source, replacement in _POSE_NORMALIZE_REPLACEMENTS:
        pose_text = pose_text.replace(source, replacement)
    return pose_text


def _normalize_pose_list(options: list[str]) -> list[str]:
    return [_normalize_pose_text(t) for t in options]


def pose_expression_options_by_aspect(scale: str, shot: str, aspect: str) -> list[str]:
    if aspect == "landscape":
        scale_options = landscape_pose_expression_options.get(_pose_pool_scale(scale), {})
        if shot in scale_options:
            return _normalize_pose_list(_options_for_aspect(scale_options[shot], aspect))
    return _normalize_pose_list(_options_for_aspect(POSE_EXPRESSION_OPTIONS[_pose_pool_scale(scale)][shot], aspect))


def scene_light_options_by_aspect(scale: str, shot: str, aspect: str):
    options = SCENE_LIGHT_OPTIONS[_pool_scale(scale)][shot]
    if isinstance(options, dict) and any(key in options for key in ("室内", "室外", "野外", "default")):
        return {
            group: _options_for_aspect(group_options, aspect)
            for group, group_options in options.items()
            if group in ("室内", "室外", "野外", "default")
        }
    return _options_for_aspect(options, aspect)


