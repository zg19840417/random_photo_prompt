def _clean_pose_family_conflicts(text: str) -> str:
    pose = _apply_broken_phrase_replacements(text)
    clauses = _clauses(pose)
    if not clauses:
        return pose
    is_seated_or_kneeling = any(marker in pose for marker in _SEATED_OR_KNEELING_MARKERS)
    if is_seated_or_kneeling:
        clauses = [
            clause
            for clause in clauses
            if not any(marker in clause for marker in _STANDING_LEG_CONFLICT_MARKERS)
        ]
        clauses = [
            clause.replace("脚部落点完整可见", "腿部姿态清楚")
            for clause in clauses
        ]
    return "，".join(clauses)


def _append_missing_clauses(text: str, clauses: tuple[str, ...], limit: int) -> str:
    current = str(text or "").strip("，。 \n\t")
    for clause in clauses:
        if clause and clause not in current and len(current) + len(clause) + 1 <= limit:
            current = f"{current}，{clause}" if current else clause
    return current


