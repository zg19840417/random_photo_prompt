def _fix_outfit_aesthetic_conflicts(text: str, scale: str, shot: str) -> str:
    outfit = str(text or "")
    if scale != "bold":
        return outfit
    if shot not in {"large_half_body", "full_body"}:
        return outfit
    has_stocking = any(marker in outfit for marker in ("吊带丝袜", "长筒丝袜", "连裤袜", "过膝长袜"))
    if not has_stocking:
        return outfit
    conflict_markers = (
        "工装短裤",
        "运动短裤",
        "运动短裙",
        "牛仔短裤",
        "直筒短裤",
        "热裤",
        "工字肩带",
        "运动风短背心",
    )
    if not any(marker in outfit for marker in conflict_markers):
        return outfit
    replacements = (
        ("炭灰高腰工装短裤", "炭灰高腰西装短裙"),
        ("白色高腰直筒短裤", "白色高腰缎面短裙"),
        ("白色高腰牛仔短裤", "白色高腰A字短裙"),
        ("浅灰色高腰热裤", "浅灰色高腰包臀短裙"),
        ("浅灰色低腰热裤", "浅灰色低腰包臀短裙"),
        ("白色低腰运动短裙", "白色低腰百褶短裙"),
        ("运动风短背心", "亮面短款吊带背心"),
        ("裤侧有小口袋，", ""),
        ("裤腰有金属扣和双道压线", "裙腰有窄压线"),
        ("裤腰有银色纽扣和浅色明线", "裙腰有银色纽扣和浅色压线"),
        ("裤脚有细窄翻边", "裙摆有细窄滚边"),
        ("裤脚停在大腿上方，", "裙摆停在大腿上方，"),
    )
    for source, replacement in replacements:
        outfit = outfit.replace(source, replacement)
    outfit = re.sub(r"，{2,}", "，", outfit)
    return outfit.strip("，、 \n\t")


