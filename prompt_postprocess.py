def _dedupe_full_body_camera(text: str) -> str:
    source = str(text or "")
    if source.strip("，。 \n\t") == "全身镜头":
        return "全身镜头"
    if source.strip("，。 \n\t") == "竖向全身写真构图":
        return "竖向全身写真构图"
    clauses = _clauses(source)
    if not clauses:
        return str(text or "")
    full_body_clauses = [clause for clause in clauses if "全身" in clause and "构图" in clause]
    if full_body_clauses:
        clauses = [clause for clause in clauses if clause not in {"全身镜头", "全身像"}]
    if len(full_body_clauses) < 2:
        return _camera_first_clause("，".join(clauses))
    specific = next((clause for clause in full_body_clauses if clause not in {"全身大片构图", "横向全身大片构图"}), full_body_clauses[-1])
    kept = [clause for clause in clauses if not ("全身" in clause and "构图" in clause)]
    kept.insert(0, specific)
    return _camera_first_clause("，".join(kept))


