def _dedupe_clauses(text: str) -> str:
    clauses = _clauses(text)
    deduped: list[str] = []
    seen: set[str] = set()
    for clause in clauses:
        key = re.sub(r"\s+", "", clause)
        if key and key not in seen:
            deduped.append(clause)
            seen.add(key)
    return "，".join(deduped)


